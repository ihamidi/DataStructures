
public class Pair {
	//pair has key and address
	public int key;
	public long address;
	//constructor for pair
	public Pair(int key, long address) {
		this.key = key;
		this.address = address;
	}
	public Pair() {
		
	}
	//returns key
	public int getKey() {
		return key;
	}

	//returns address
	public long getAddress() {
		return address;
	}
	

}
